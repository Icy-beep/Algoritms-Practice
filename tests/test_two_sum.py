import pytest
from src.functions import two_sum


# Позитивные тесты и пограничные случаи
@pytest.mark.parametrize('nums, target, expected', [
    # Позитивные
    ([2, 7, 11, 15], 9, [0, 1]),  # Обычный случай
    ([3, 2, 4], 6, [1, 2]),  # Числа не в начале
    ([3, 3], 6, [0, 1]),  # Одинаковые числа
    ([1, 5, 8], 13, [1, 2]),  # Сумма последних элементов
    ([-1, -2, -3, -4, -5], -8, [2, 4]),  # Отрицательные числа
    ([-5, 4, 8, 10], 5, [0, 3]),  # Смешанные знаки
    ([0, 4, 3, 0], 0, [0, 3]),  # Работа с нулями
    ([10, -10], 0, [0, 1]),  # Сумма дает ноль

    # Пограничные случаи
    ([1, 2], 3, [0, 1]),  # Минимальная длина массива
    ([1, 2, 3, 4, 100], 104, [3, 4]),  # Большие числа
    ([5], 5, None),  # Один элемент
    ([1, 2, 3], 10, None),  # Решения нет
    ([], 0, None),  # Пустой массив
    ([1, 2, 3, 4], 7, [2, 3]),  # Несколько пар (первая найденная)
])
def test_two_sum_results(nums, target, expected):
    assert two_sum(nums, target) == expected


# Негативные тесты
@pytest.mark.parametrize('nums, target', [
    ("not a list", 5),  # Вместо списка строка
    ([1, "2", 3], 5),  # Строка внутри списка
    ([1, 2.5, 3], 5),  # Float внутри списка
    (None, 5),  # Вместо списка None
    ([1, 2], "3"),  # Target — строка
    ([1, [2], 3], 3),  # Вложенный список
])
def test_two_sum_type_errors(nums, target):
    with pytest.raises(TypeError):
        two_sum(nums, target)