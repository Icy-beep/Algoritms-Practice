import pytest
from hw_5_2.src.functions import is_palindrome

# Позитивные и негативные случаи результата
@pytest.mark.parametrize('x, expected', [
    # Позитивные (палиндромы)
    (121, True),      # Палиндром
    (0, True),        # Ноль — палиндром
    (11, True),       # Двузначный палиндром
    (12321, True),    # Нечетное количество цифр
    (4444, True),     # Четное количество цифр
    (1, True),        # Однозначное число
    (1221, True),     # Еще один четный

    # Негативные (не палиндромы)
    (10, False),      # Заканчивается на 0
    (123, False),     # Обычное число
    (122, False),     # Почти палиндром
    (100, False),     # Заканчивается на два нуля
    (2147483647, False), # Большое непалиндромное число

    # Особые случаи из логики функции
    (-121, False),    # Отрицательные всегда False
    (-1, False),      # Отрицательное однозначное
    (2112, True),     # Зеркальное
])
def test_is_palindrome_results(x, expected):
    assert is_palindrome(x) == expected